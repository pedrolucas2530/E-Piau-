def carregar_municipios(caminho_cache: Path = CACHE_MUNICIPIOS) -> list[dict[str, str]]:
    caminho_cache.parent.mkdir(parents=True, exist_ok=True)
    if caminho_cache.exists():
        return json.loads(caminho_cache.read_text(encoding="utf-8"))

    if requests is None:
        return MUNICIPIOS_RESERVA

    try:
        resposta = requests.get(URL_MUNICIPIOS_IBGE, timeout=20)
        resposta.raise_for_status()
        carga: list[dict[str, Any]] = resposta.json()
        municipios = [
            {"id": str(item["id"]), "nome": str(item["nome"])}
            for item in carga
            if item.get("id") and item.get("nome")
        ]
        caminho_cache.write_text(
            json.dumps(municipios, ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
        return municipios
    except (requests.RequestException, ValueError, KeyError):
        return MUNICIPIOS_RESERVA


def indice_municipios(
    municipios: list[dict[str, str]] | None = None,
) -> dict[str, dict[str, str]]:
    municipios = municipios or carregar_municipios()
    return {normalizar_chave(item["nome"]): item for item in municipios}
